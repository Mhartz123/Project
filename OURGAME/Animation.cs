		public static void UI(int i, string[] a, string b) {
			if(Enemy.Enemy_Name == "Goblin" || Enemy.Enemy_Name == "Kobold" || Enemy.Enemy_Name == "Goblin Shaman" || Enemy.Enemy_Name == "Orc" || Enemy.Enemy_Name == "Treant") {
				Encounter.Floor("1ST");
			} else if(Enemy.Enemy_Name == "Apophis") {
				Encounter.Floor("LAST");
			} else {
				Encounter.Floor("2ND");
			}
			Console.WriteLine("[{0}]", Enemy.Enemy_Name);
			Encounter.enemy_HP_check();
			Console.WriteLine("\n================================================================");
			Console.Write(a[i]);
			Console.WriteLine("\n================================================================");
			Console.WriteLine("Text : [{0}]",b);
			Encounter.check_stats();
			Encounter.UI2();
		}
		
